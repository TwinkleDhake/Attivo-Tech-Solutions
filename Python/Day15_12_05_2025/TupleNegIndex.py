""" Q4. From the tuple sequence = (10, 20, 30, 40, 50, 60, 70, 80), extract alternate elements starting from the last element 
and moving towards the first using negative indexing. """
sequence = (10, 20, 30, 40, 50, 60, 70, 80)
alternate_reversed = sequence[::-2]
print("Alternate elements in reverse order:", alternate_reversed)